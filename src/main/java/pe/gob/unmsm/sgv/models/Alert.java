
package pe.gob.unmsm.sgv.models;

public class Alert {
    private String mensaje;
    private String mensaje_ti;
    private String info;

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }

    public String getMensaje_ti() {
        return mensaje_ti;
    }

    public void setMensaje_ti(String mensaje_ti) {
        this.mensaje_ti = mensaje_ti;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
    
    
    
}
