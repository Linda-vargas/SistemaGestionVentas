
package pe.gob.unmsm.sgv.models;

public class Alert {
    private String mensaje;
    private String mensajeti;
    private String info;

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }

    public String getMensajeti() {
        return mensajeti;
    }

    public void setMensajeti(String mensajeti) {
        this.mensajeti = mensajeti;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }
    
    
    
}
